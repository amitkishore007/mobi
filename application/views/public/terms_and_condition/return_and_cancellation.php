<h2>How do I cancel an order?
</h2>
<p>
You can cancel your order online before the product has been shipped. Your entire order amount will be refunded.
</p>
<p>
In case the item you have ordered has been shipped but has not yet been delivered to you, you may still cancel the order online. Your refund will be processed once we receive the originally ordered item back from the courier.
</p>
<p>
Unfortunately, an order cannot be cancelled once the item has been delivered to you.
</p>
<p>

In order to cancel an item in your order:
<ul>
<li>
Log into your Mobicharge account and go to the 'My Orders' page</li>
<li>
Identify the item you want to cancel and click on the corresponding 'View Details' link</li>
<li>
In the detailed order page, you will see 'Cancel' links against each of the items in that order</li>
<li>
Click on the 'Cancel' link, indicate the reason for cancellation, choose a mode of refund and confirm cancellation</li>
<li>
Once your cancellation request is created, we will ensure that the cancellation is processed as soon as possible</li>
</ul>
</p>

<h2>How long will it take to process my cancellation request?</h2>
<p>	
Once you request the cancellation of item(s) in your order, it will take us a maximum of 1-2 business days to cancel the order and initiate a refund. You will be notified of the same by email.
</p>
<p>
If the Order Status shows the item(s) in your order as 'Shipped', we will process your cancellation request directly with our logistics partner. Your refund will be processed soon after we receive the cancelled items back from the courier.
</p>

<p>
However, if you opt for having the money transferred back to the source of transaction, it may take up to 7-10 business days for the respective banks to process the refund. Please get in touch with the banks directly in case of any delays post confirmation of cancellation/refund by Mobicharge.
</p>
<h2>Why do I see a disabled 'Cancel' link?</h2>

<p>
A disabled 'Cancel' link can mean the following things: <br/>	
<b> The item is being shipped together with another order of yours</b><br/>
<b>The item is non-refundable (eg. e-Gift Vouchers)</b>
</p>
<p>
In any of the above cases, you can get in touch with our Customer Support for more information
What are the modes of refund available after cancellation?
In order to confirm cancellation of item(s) in your order, you need to indicate your refund preference.</p>

<p>
There are two modes of refund:
Mobicharge Wallet - If you choose this option, the amount will be added to your Mobicharge Wallet in the 'store credit' component.</p>
<p>
Back to Source - In this case, the money will be refunded back to the payment mode/account that was originally used to make the transaction.
</p>
<p>
Once you have requested the cancellation of item(s) in your order, Mobicharge will complete the cancellation and initiate the refund, depending on your preference.
</p>

<h2>What is the cancellation and refund policy for eBooks?</h2>
<p>
If you have already purchased an eBook but don't have a compatible device to download and read it on, you can revoke the purchase under the 30 Day Cancellation & Refund Policy for eBooks.
</p>
<p>
Cancellation and subsequent refund will also go through if we are unable to resolve a genuine technical fault within 2 business days from the the time you had reported the error.
</p>
