<h5><b>Orders</b>
</h5>
<h5>
I missed the delivery of my order today. What should I do?</h5>
<p>
The courier service delivering your order usually tries to deliver on the next business day in case you miss a delivery.
You can check your SMS for more details on when the courier service will try to deliver again.
</p>
<h5>Will the delivery be tried again if I'm not able to collect my order the first time?</h5>
<p>
Couriers make sure that the delivery is re-attempted the next working day if you can't collect your order the first time.</p>
<h5>
The delivery of my order is delayed. What should I do?</h5>
<p>
On the rare occasion that your order is delayed, please check your email & messages for updates. A new delivery timeframe will be shared with you and you can also track its status by visiting My Orders.</p>
<h5>
What should I do if my order is approved but hasn't been shipped yet?</h5>
<p>
Sellers usually ship orders 1-2 business days before the delivery date so that they reach you on time. In case your order hasn't been shipped within this time please contact our Customer Support so that we can look into it.</p>

<h5>Can I take the shipment after opening and checking the contents inside?</h5>
<p>
As per company policy, a shipment can't be opened before delivery, but you can accept the shipment and get in touch with us later in case you have any concerns.</p>

<h5>How do I know my order has been confirmed?</h5>
<p>	
An e-mail & SMS will be sent once you've successfully placed your order. We'll also let you know as soon as the seller ships the item(s) to you along with the tracking number(s) for your shipment(s). You can track your orders from the 'My Orders' section on your Mobicharge account.</p>


<h5>How quickly can I get my order delivered?</h5>
<p>
Orders will be delivered by the date you see on the product page for your location.
My order has reached the nearest delivery hub, but why isn't it out for delivery yet?
The courier service delivering your order are responsible for making sure that your order reaches you within the delivery date. Rest assured, you'll get an SMS once your shipment is out for delivery.</p>

<h5>My order status shows 'Out for Delivery', but I haven't got it yet. When will I get it?</h5>
<p>
The courier service will contact you for delivery of your order. Please check your SMS for more details.</p>

<h5>Why can't I track my order even though it has been shipped?</h5>
<p>
Courier services usually take upto 24 hours to activate tracking for an order once it's shipped. Please check again after the mentioned time frame.
</p>
<h5>
Can I get my orders delivered at a specific time?</h5>
<p>
Please coordinate with the courier executive delivering your order to see if this is possible. Delivering at a specific time may not always be an option from courier services due to their schedules.</p>
<h5>
What should I do if I don't get the invoice for my order?</h5>
<p>
Sellers send a hard copy of the invoice in the shipments. A soft copy is also emailed to you within 24 hours of delivery in the delivery confirmation email sent to your registered email ID.
You can also visit My Account › My Orders page to get invoices for your orders.
</p>
<h5>

How do I get invoices for my previous orders without having to check my e-mails?</h5>
<p>
You can easily send invoices to your email ID from your Mobicharge account. To do so, visit My Account › Orders, choose the order for which you'd like the invoice for and click on the 'E-mail Invoice' option.</p>


<h5>Should I call Mobicharge's Customer Care for installation once my product is delivered?</h5>
<p>
To make sure that you have a smooth and hassle-free experience, the installation is automatically arranged for your product by the seller within 2-5 business after the item is delivered. You will also get an SMS with the exact installation details after delivery.
Will my product be installed by the brand or Mobicharge? If it is installed by Mobicharge, will it affect the warranty of the product?
Your product will be installed by the brand's authorised service partner. Rest assured, the warranty for the product will not be affected when an item is installed by the authorised partners.
</p>
<h5>
How will my order be delivered?</h5>
<p>
All orders are shipped by sellers through courier services who deliver the packages to your doorstep or the nearest pick-up store if the option is selected.
Why is the 'Cash on Delivery' payment mode not available at my location?
Availability of the 'Cash on Delivery' payment mode depends on the courier services delivering to your location. Please enter your pincode on the product page to check if this payment mode is available at your location. Courier service providers also have limits on the amount you can pay through cash on delivery based on the destination and your order could have exceeded this limit.
</p>
<h5>Where can I find the seller's returns policy?</h5>
<p>
You can see the respective seller's Returns policy on the product page.</p>
<h5>
What does the tag 'Imported' beside a product mean?</h5>
<p>
Sometimes items have to be sourced by sellers from their international partners. Such items have the tag 'Imported' on the product page and can take at least 10 or more days to be delivered.
</p>
<h5>
How can I place an order?</h5>
<p>
To place an order, please follow these steps:
<ul>
<li> Select the product you'd like to buy and 'Check Availability at' your preferred pincode</li>
<li> Add products to your cart or just hit 'Buy Now'</li> 
<li> Choose or 'Add delivery address'. Use a preferred payment mode and confirm the order</li>
</ul>
</p>
<p>
I have a complaint about the courier executive who came to deliver my order.
You can contact our Customer Support team with the details and we will get in touch with the courier service provider to resolve your complaint.
</p>


